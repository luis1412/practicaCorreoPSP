    private void botonConectarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonConectarActionPerformed

    establecerPropiedades();
        
        if (conectado == false) {
    String tlsT = (tls2 ? "true" : "false");
    emailSend = new EmailSend(servidorSmtp2 ,usuario2, clave2, puerto2 + "", tlsT, destinatario2, cuerpo2, asunto2);
            if (!(servidorSmtp2.isEmpty() || puerto2 <= 0 && usuario2.isEmpty() || clave2.isEmpty() || remitente2.isEmpty() || destinatario2.isEmpty())) {
                try {
                    if (emailSend.conectar()) {
                        JOptionPane.showMessageDialog(this, "Conectado con exito bienvenido " + usuario2.split("@")[0]);
                        botonConectar.setText("Desconectar");
                        conectado = true;
                        enviarMensaje.setEnabled(true);
                        cuerpoMensaje.setEnabled(true);
                        botonAdjuntar.setEnabled(true);
                        asunto.setEnabled(true);
                    }
                } catch (MessagingException ex) {
                    Logger.getLogger(servidor.class.getName()).log(Level.SEVERE, null, ex);
                    JOptionPane.showMessageDialog(this, "No se ha podido conectar");
                    borrarCampos();
                }

            }
            else{
                JOptionPane.showMessageDialog(this, "Completa todos los campos");
            }
        } else {
            conectado = false;
            botonConectar.setText("Conectar");
            enviarMensaje.setEnabled(false);
            cuerpoMensaje.setEnabled(false);
            botonAdjuntar.setEnabled(false);
            rutaFichero = null;
            asunto.setEnabled(false);
            emailSend = null;
            borrarCampos();
        }


    }//GEN-LAST:event_botonConectarActionPerformed

    
